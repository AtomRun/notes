package com.leeup.javase.day05.数组;
/**
 * 数组
 * @author 李闯
 *
 */
public class Demo1_Array {

	public static void main(String[] args) {
		/**
		 * 没学数组之前，我们使用变量保存值
		 */
		int x = 10;
		x = 20; 
		System.out.println(x);
		
		//数据类型[] 数组名 = new 数据类型[数组的长度];
		int [] arr = new int[5];
		
		/**
		 * 左边开始
		 * int：代表数组存储的值的数据类型，即数据类型
		 * []：代表的数组，几个中括号就代表几维数组
		 * arr：合法的标识符，数组的别名
		 * 
		 * 右边
		 * new：创建新的实体或对象
		 * int：数据类型
		 * []：代表的是数组
		 * 5：代表的数组的长度，在这里表示数组中能够存几个int数
		 */
	}
}
